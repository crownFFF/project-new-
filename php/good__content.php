        <h2>照顧方式</h2>
        <hr>
        <p>
        <h3>1.土壤</h3>
        盆土要求疏鬆肥沃、排水良好，可用園土、腐葉土、蛭石、粗沙加少量骨粉混合配製，再加入適量雞糞或牛糞作為基肥。每1~2年春季換盆一次，保證土壤的肥力。
        <br>
        <h3>2.溫度</h3>
        溫度過高或過低都不利於星王子正常生長，冬天室溫不得低於10度，夏季氣溫高於35度時，注意加強通風，降溫養護。
        <br>
        <h3>3.光照</h3>
        適宜的光照對星王子來說很重要。光照不足影響它的葉色和光澤，光照太強又會灼傷葉片，影響觀賞性。冬季陽光比較溫和，可放置於室內向陽處，給予其充足光照；夏季則需遮陰養護，放置於涼爽、通風的半陰處，避免烈日暴曬。
        <br>
        <h3>4.澆水</h3>
        星王子喜乾燥不喜水溼，澆水需適量，保持盆土溼潤即可，不可有積水。夏天休眠期需嚴格控制澆水，保持盆土偏幹。
        <br>
        <h3>5.施肥</h3>
        星王子生長期可毎15天施一次腐熟的稀薄液肥，以磷鉀肥為主，濃度不宜過高，以免造成莖葉徒長，株型鬆散，遵循薄肥勤施的原則，夏天溫度過高時停止施肥，冬天溫度太低時也不可施肥，避免造成肥害。
        </p>

